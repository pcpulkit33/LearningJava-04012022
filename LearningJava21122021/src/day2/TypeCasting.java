package day2;

public class TypeCasting {

	public static void main(String[] args) {
		
		//TypeCasing - When you want to convert one value of a type to another datatype
		//double >> float >> long >> int >> byte >> short
		
		int num = 543534;
		
		long longNum = num;  
		
		double doubleNum = 3434.44;
		
		int intNum = (int)doubleNum;
		System.out.println(intNum);
	}

}
